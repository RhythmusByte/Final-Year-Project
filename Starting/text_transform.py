message = 'My name is'
name = 'Akhil mahesh'

# This prints the message and name
print(message + ' ' + name) 

# Print the name's first letters to capital
print(message + ' ' + name.title())

# Print the name in uppercase
print(message + ' ' + name.upper())

# Print the name in lowercase
print(message + ' ' + name.lower())

# Prints the index number 8th character
print(message[8])
